productRouter.post(
    '/slug/:id/reviews',
    isAuth,
    expressAsyncHandler(async (req, res) => {
        const productId = req.params.id;
        const product = await Product.findById(productId);
        if (product) {
            if (product.customerReviews.find((x) => x.name === req.user.name)) {
                return res
                    .status(400)
                    .send({ message: 'You already submitted a review' });
            }
            const review = {
                name: req.user.name,
                rating: Number(req.body.rating),
                comment: req.body.comment,
            };
            product.customerReviews.push(review);
            product.reviews = product.customerReviews.length;
            const rating =
                product.customerReviews.reduce((a, c) => c.rating + a, 0) /
                product.customerReviews.length;
            product.rating = rating.toFixed(1);
            const updatedProduct = await product.save();
            res.status(201).send({
                message: 'Review Created',
                review: updatedProduct.customerReviews[updatedProduct.customerReviews.length - 1],
            });
        } else {
            res.status(404).send({ message: 'Product Not Found' });
        }
    })
);

productRouter.patch(
  "/:id",
  isAuth,
  expressAsyncHandler(async (req, res) => {
    const productId = req.params.id;
    const count = await Product.updateOne({ _id: productId }, req.body);
    if (count.modifiedCount)
      res.status(200).send({ message: "Product quantity reduced" });
    else if (!count.matchedCount)
      res.status(404).send({ message: "Product Not Found" });
    else res.status(400).send({ message: "Uncaught error" });
  })
);

